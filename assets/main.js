function setupNavigation() {
  const toggle = byId("nav-toggle");
  const nav = byId("site-nav");
  const navLinks = Array.from(document.querySelectorAll(".nav-link"));
  const sections = navLinks
    .map((link) => document.querySelector(link.getAttribute("href")))
    .filter(Boolean);

  const setActiveLink = (activeId) => {
    navLinks.forEach((link) => {
      const isActive = link.getAttribute("href") === `#${activeId}`;
      link.classList.toggle("is-active", isActive);
      if (isActive) {
        link.setAttribute("aria-current", "page");
      } else {
        link.removeAttribute("aria-current");
      }
    });
  };

  const getHeaderOffset = () => {
    const header = byId("top");
    return header ? header.offsetHeight + 12 : 96;
  };

  const scrollToSection = (target) => {
    if (!target) return;
    const top = target.getBoundingClientRect().top + window.scrollY - getHeaderOffset();
    window.scrollTo({
      top: Math.max(0, top),
      behavior: prefersReducedMotion ? "auto" : "smooth",
    });
  };

  const closeMenu = () => {
    nav.classList.remove("is-open");
    toggle.classList.remove("is-open");
    toggle.setAttribute("aria-expanded", "false");
    toggle.setAttribute("aria-label", "打开导航菜单");
    document.body.classList.remove("menu-open");
  };

  const openMenu = () => {
    nav.classList.add("is-open");
    toggle.classList.add("is-open");
    toggle.setAttribute("aria-expanded", "true");
    toggle.setAttribute("aria-label", "关闭导航菜单");
    document.body.classList.add("menu-open");
  };

  toggle.addEventListener("click", () => {
    if (nav.classList.contains("is-open")) {
      closeMenu();
    } else {
      openMenu();
    }
  });

  const hashLinks = Array.from(document.querySelectorAll('a[href^="#"]')).filter(
    (link) => link.getAttribute("href") !== "#"
  );

  hashLinks.forEach((link) => {
    link.addEventListener("click", (event) => {
      if (link.classList.contains("is-disabled") || link.getAttribute("aria-disabled") === "true") {
        return;
      }

      const selector = link.getAttribute("href");
      const target = document.querySelector(selector);
      if (!target) return;

      event.preventDefault();
      scrollToSection(target);

      if (link.classList.contains("nav-link")) {
        setActiveLink(target.id);
      }

      if (window.innerWidth <= 820 && nav.contains(link)) {
        closeMenu();
      }
    });
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") closeMenu();
  });

  document.addEventListener("click", (event) => {
    if (window.innerWidth > 820 || !nav.classList.contains("is-open")) return;
    if (nav.contains(event.target) || toggle.contains(event.target)) return;
    closeMenu();
  });

  let isTicking = false;
  const syncActiveSection = () => {
    const checkpoint =
      window.scrollY + getHeaderOffset() + Math.min(window.innerHeight * 0.18, 160);
    let activeId = sections[0]?.id ?? "home";

    sections.forEach((section) => {
      if (section.offsetTop <= checkpoint) {
        activeId = section.id;
      }
    });

    const pageBottom = window.innerHeight + window.scrollY;
    const documentHeight = document.documentElement.scrollHeight;
    if (pageBottom >= documentHeight - 4) {
      activeId = sections[sections.length - 1]?.id ?? activeId;
    }

    setActiveLink(activeId);
    isTicking = false;
  };

  const requestSync = () => {
    if (isTicking) return;
    isTicking = true;
    window.requestAnimationFrame(syncActiveSection);
  };

  window.addEventListener("scroll", requestSync, { passive: true });
  window.addEventListener("resize", requestSync);
  requestSync();
}

function setupReveal() {
  const revealElements = Array.from(document.querySelectorAll(".reveal"));

  if (prefersReducedMotion) {
    revealElements.forEach((element) => element.classList.add("visible"));
    return;
  }

  const revealObserver = new IntersectionObserver(
    (entries, observer) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      });
    },
    {
      threshold: 0.18,
      rootMargin: "0px 0px -10% 0px",
    }
  );

  revealElements.forEach((element) => revealObserver.observe(element));
}

function setupBackToTop() {
  const button = byId("back-to-top");

  const syncVisibility = () => {
    button.classList.toggle("is-visible", window.scrollY > 520);
  };

  syncVisibility();
  window.addEventListener("scroll", syncVisibility, { passive: true });

  button.addEventListener("click", () => {
    window.scrollTo({
      top: 0,
      behavior: prefersReducedMotion ? "auto" : "smooth",
    });
  });
}

